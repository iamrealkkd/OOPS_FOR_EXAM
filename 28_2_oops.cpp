// OOPs (Object-Oriented Programming System)
// Object-Oriented Programming System is a programming approach based on classes and objects.

// OOPs is a programming paradigm based on real-world objects.

// It uses classes and objects to structure programs.

// It helps in code reusability and modularity.

// It improves code security using data hiding.

// It makes programs easier to maintain and extend.

// Main Four Pillars of OOP are:
// Encapsulation: Wrapping up of data and member functions together in a class.

// Abstraction: Hiding internal details and showing only necessary features.

// Inheritance: One class can inherit properties of another class.

// Polymorphism: One function behaves differently in different situations.